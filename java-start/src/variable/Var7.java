package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) true, false
        char d = 'A'; // 문자 하나, 작은따옴표
        String e = "Hello Java"; // 문자열, 문자열을 다루기 위한 특별한 타입, 큰따옴표

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        // 프로그래밍언어에서 리터럴은 직접 적은 '고정된 값'
        // 개발자가 직접 적은 100,10.5, true 을 리터럴이라 함. 따라서 리터럴 자체는 변하지 않는다.

    }
}
